import UIKit

extension UIFont {
  static func circularStdBook(size: CGFloat) -> UIFont {
    return UIFont(name: "CircularStd-Book", size: size)!
  }

  static func circularStdBookItalic(size: CGFloat) -> UIFont {
    return UIFont(name: "CircularStd-BookItalic", size: size)!
  }

  static func circularStdMedium(size: CGFloat) -> UIFont {
    return UIFont(name: "CircularStd-Medium", size: size)!
  }

  static func circularStdBold(size: CGFloat) -> UIFont {
    return UIFont(name: "CircularStd-Bold", size: size)!
  }

  static func sfMonoRegular(size: CGFloat) -> UIFont {
    return UIFont(name: "SFMono-Regular", size: size)!
  }
}
